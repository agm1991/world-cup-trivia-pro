// Helper function to get questions by level
export const getWorldCupQuestionsByLevel = (level: number): Question[] => {
  // Get all easy questions for levels 1-10 (all easy mode)
  const easyQuestions = worldCupQuestions.filter(q => q.difficulty === 'easy');
  
  if (level <= 10) {
    // Levels 1-10: All easy mode, different questions for each level
    // Level 1: questions 0-9, Level 2: questions 10-19, etc.
    const startIndex = (level - 1) * 10;
    const endIndex = startIndex + 10;
    
    // If we don't have enough questions, cycle through them
    if (endIndex <= easyQuestions.length) {
      return easyQuestions.slice(startIndex, endIndex);
    } else {
      // Cycle through available questions
      const questions: Question[] = [];
      for (let i = 0; i < 10; i++) {
        questions.push(easyQuestions[(startIndex + i) % easyQuestions.length]);
      }
      return questions;
    }
  }

  // Ultimate Mode (levels 31–40): 1950–1966 World Cups only (knockouts, shocks, records)
  if (level >= 31 && level <= 40) {
    const start = (level - 31) * 10;
    return worldCupLevels31to40QuestionsFlat.slice(start, start + 10);
  }

  // Finals Mode (levels 41–50): expert pool — tough mixed trivia, all World Cups 1930–2022
  if (level >= 41 && level <= 50) {
    const start = (level - 41) * 10;
    return worldCupFinalsModeQuestionsFlat.slice(start, start + 10);
  }

  // Levels 11+: Use difficulty-based filtering
  let difficulty: 'easy' | 'medium' | 'hard';
  if (level <= 20) difficulty = 'medium';
  else difficulty = 'hard';
  
  const filteredQuestions = worldCupQuestions.filter(q => q.difficulty === difficulty);
  
  // For levels 11+, distribute questions to avoid duplicates
  const startIndex = ((level - 1) % Math.ceil(filteredQuestions.length / 10)) * 10;
  const endIndex = Math.min(startIndex + 10, filteredQuestions.length);
  
  if (endIndex <= filteredQuestions.length) {
    return filteredQuestions.slice(startIndex, endIndex);
  } else {
    // Cycle through if needed
    const questions: Question[] = [];
    for (let i = 0; i < 10; i++) {
      questions.push(filteredQuestions[(startIndex + i) % filteredQuestions.length]);
    }
    return questions;
  }
};

export const getWorldCupQuestionForLevel = (level: number, questionIndex: number): Question | undefined => {
  const questions = getWorldCupQuestionsByLevel(level);
  return questions[questionIndex % questions.length];
};
