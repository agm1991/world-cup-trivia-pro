export const getStadiumsQuestionsByLevel = (level: number): Question[] => {
  const authorLevel = getStadiumsAuthorLevel(level);
  if (authorLevel?.length) {
    return authorLevel;
  }

  // Get all easy questions for levels 1-10 (all easy mode)
  const easyQuestions = stadiumsQuestions.filter(q => q.difficulty === 'easy');
  
  if (level <= 10) {
    // Levels 1-10: All easy mode, different questions for each level
    const startIndex = (level - 1) * 10;
    const endIndex = startIndex + 10;
    
    if (endIndex <= easyQuestions.length) {
      return easyQuestions.slice(startIndex, endIndex);
    } else {
      // Cycle through available questions
      const questions: Question[] = [];
      for (let i = 0; i < 10; i++) {
        questions.push(easyQuestions[(startIndex + i) % easyQuestions.length]);
      }
      return questions;
    }
  }
  
  // Levels 11-20: Medium difficulty
  if (level <= 20) {
    const mediumQuestions = stadiumsQuestions.filter(q => q.difficulty === 'medium');
    const startIndex = (level - 11) * 10;
    const endIndex = startIndex + 10;
    
    if (endIndex <= mediumQuestions.length) {
      return mediumQuestions.slice(startIndex, endIndex);
    } else {
      // Cycle through available questions
      const questions: Question[] = [];
      for (let i = 0; i < 10; i++) {
        questions.push(mediumQuestions[(startIndex + i) % mediumQuestions.length]);
      }
      return questions;
    }
  }
  
  // Levels 21-30: Hard difficulty
  const hardQuestions = stadiumsQuestions.filter(q => q.difficulty === 'hard');
  const startIndex = (level - 21) * 10;
  const endIndex = startIndex + 10;
  
  if (endIndex <= hardQuestions.length) {
    return hardQuestions.slice(startIndex, endIndex);
  } else {
    // Cycle through available questions
    const questions: Question[] = [];
    for (let i = 0; i < 10; i++) {
      questions.push(hardQuestions[(startIndex + i) % hardQuestions.length]);
    }
    return questions;
  }
};
