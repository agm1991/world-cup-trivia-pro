/**
 * World Cup finals — player performance only (goals, assists, cards, minutes, match outcomes involving the player).
 * Batch 1: Algeria, Australia, Argentina (Select a Legend). No host-nation or stadium trivia.
 * Stems focus on what the player did; prefer opponent-based prompts over plain yes/no score checks.
 */
import type { Question } from '@/types/game';

function pq(
  id: string,
  year: number,
  question: string,
  opts: [string, string, string, string],
  correct: 'A' | 'B' | 'C' | 'D',
  hints: [string, string, string],
  difficulty: 'easy' | 'medium' | 'hard' = 'medium',
): Question {
  return {
    id,
    category: 'player',
    difficulty,
    question,
    optionA: opts[0],
    optionB: opts[1],
    optionC: opts[2],
    optionD: opts[3],
    correctAnswer: correct,
    hint1: hints[0],
    hint2: hints[1],
    hint3: hints[2],
    eventYear: year,
  };
}

