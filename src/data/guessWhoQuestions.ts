// Questions organized by level (1–40): 1–10 easy; 11–20 medium; 21–30 hard; 31–40 ultimate (text-only).

export const getGuessWhoQuestionsByLevel = (level: number): Question[] => {
  const levelFromId = (q: Question) => parseInt(q.id.split('-')[2] || '1', 10);

  if (level <= 10) {
    const levelQuestions = guessWhoQuestions.filter(
      (q) => levelFromId(q) === level && q.difficulty === 'easy'
    );
    return levelQuestions.slice(0, 10);
  }

  if (level <= 20) {
    const levelQuestions = guessWhoQuestions.filter(
      (q) => levelFromId(q) === level && q.difficulty === 'medium'
    );
    return levelQuestions.length > 0 ? levelQuestions.slice(0, 10) : [];
  }

  const levelQuestions = guessWhoQuestions.filter((q) => levelFromId(q) === level);
  return levelQuestions.length > 0 ? levelQuestions : [];
};
