/** Levels 1–10: curated 2014–2022 (`curatedScorelineLevels1to10`). Levels 11–24 from `curatedScorelineLevels11to30`; 25–30 from `curatedScorelineLevels25to50`; 31–50 from `curatedScorelineLevels25to50`. */
const YEARS_LEVELS_1_10 = new Set(['2014', '2018', '2022']);

function yearFromScorelineId(q: Question): string | null {
  const p = q.id.split('-');
  return p.length >= 2 ? p[1]! : null;
}

/** `sc-YEAR-SUBLEVEL-n` — sublevel 5 is the “obscure” tier; 1–4 are finals / knockouts / memorable games. */
function sublevelFromScorelineId(q: Question): number | null {
  const p = q.id.split('-');
  if (p.length < 3) return null;
  const n = parseInt(p[2]!, 10);
  return Number.isNaN(n) ? null : n;
}

/** 2014 / 2018 / 2022 only, excluding obscure sublevel-5 rows. */
function isFamousTier2014to2022(q: Question): boolean {
  const y = yearFromScorelineId(q);
  if (!y || !YEARS_LEVELS_1_10.has(y)) return false;
  const sl = sublevelFromScorelineId(q);
  return sl !== null && sl >= 1 && sl <= 4;
}

