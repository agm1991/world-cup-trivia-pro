/** Deterministic PRNG (stable shuffle across builds/sessions). */
function mulberry32(seed: number): () => number {
  return () => {
    let t = (seed += 0x6d2b79f5);
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

function shuffleDeterministic<T>(items: readonly T[], seed: number): T[] {
  const arr = [...items];
  const rnd = mulberry32(seed);
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(rnd() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

function chunkQuestions(qs: Question[], chunkSize: number): Question[][] {
  const out: Question[][] = [];
  for (let i = 0; i < qs.length; i += chunkSize) {
    out.push(qs.slice(i, i + chunkSize));
  }
  return out;
}

/** Shuffle full pool, chunk into levels, then shuffle each level’s order (different seed per level). */
function buildLeveledQuestions(
  pool100: Question[],
  poolShuffleSeed: number,
  perLevelSeedBase: number,
): Question[][] {
  const shuffledPool = shuffleDeterministic(pool100, poolShuffleSeed);
  const chunks = chunkQuestions(shuffledPool, 10);
  return chunks.map((chunk, idx) => shuffleDeterministic(chunk, perLevelSeedBase + idx * 9973));
}

/**
 * Levels 1–3: only sublevel 1 (10 per year × 3 = 30) — all easy, finals & headline knockouts.
 * Levels 4–10: 70 from sublevels 2–4 (famous tier), excluding level 1–3 ids.
 */
function buildLevels1to10Famous2014to2022(all: Question[]): Question[][] {
  const famous = all.filter(isFamousTier2014to2022);
  const sub1 = famous.filter((q) => sublevelFromScorelineId(q) === 1);
  const shuffledSub1 = shuffleDeterministic(sub1, 0x5c0fa111);
  const levels123 = chunkQuestions(shuffledSub1, 10);

  const fromSub2to4 = famous.filter((q) => (sublevelFromScorelineId(q) ?? 0) >= 2);
  const shuffledRest = shuffleDeterministic(fromSub2to4, 0x5c0fa422);
  const for410 = shuffledRest.slice(0, 70);
  const levels410 = chunkQuestions(for410, 10);

  const out: Question[][] = [];
  for (let i = 0; i < 3; i++) {
    out.push(shuffleDeterministic(levels123[i] ?? [], 101_000 + i * 9973));
  }
  for (let i = 0; i < 7; i++) {
    out.push(shuffleDeterministic(levels410[i] ?? [], 101_000 + (i + 3) * 9973));
  }
  return out;
}

function buildScorelineLevelBuckets(): LevelBuckets {
  const l1_10 = curatedScorelineLevels1to10;

  const curated11To30 = curatedScorelineLevels11to30;
  const curated25to50 = curatedScorelineLevels25to50;

  const l11_20 = curated11To30.slice(0, 10);
  const l21_30: Question[][] = [
    ...(curated11To30.slice(10, 14) ?? []),
    ...(curated25to50.slice(0, 6) ?? []),
  ];
  const l31_50 = curated25to50.slice(6, 26);

  return {
    l1_10,
    l11_20,
    l21_30,
    l31_50,
  };
}

function getScorelineLevelBuckets(): LevelBuckets {
  if (!scorelineLevelBuckets) {
    scorelineLevelBuckets = buildScorelineLevelBuckets();
  }
  return scorelineLevelBuckets;
}

export const getScorelineQuestionsByLevel = (level: number): Question[] => {
  if (level < 1 || level > 50) return [];
  const b = getScorelineLevelBuckets();
  if (level <= 10) return b.l1_10[level - 1] ?? [];
  if (level <= 20) return b.l11_20[level - 11] ?? [];
  if (level <= 30) return b.l21_30[level - 21] ?? [];
  return b.l31_50[level - 31] ?? [];
};
