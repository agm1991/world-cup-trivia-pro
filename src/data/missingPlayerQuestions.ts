/** 10 questions per level. Easy 1–10 = missingPlayerEasyLevels1To10; Easy 11–12 = missingPlayerEasyLegacy; Medium 13–18; Hard 19–30. */
export const getMissingPlayerQuestionsByLevel = (level: number): Question[] => {
  const perLevel = 10;
  if (level >= 1 && level <= 10) {
    const start = (level - 1) * perLevel;
    return missingPlayerEasyLevels1To10.slice(start, start + perLevel);
  }
  if (level >= 11 && level <= 12) {
    const pool = missingPlayerEasyLegacy;
    const startIndex = ((level - 11) * perLevel) % pool.length;
    const out = [];
    for (let i = 0; i < perLevel; i++) {
      out.push(pool[(startIndex + i) % pool.length]);
    }
    return out;
  }
  let pool;
  let startIndex;
  if (level >= 13 && level <= 18) {
    pool = missingPlayerQuestions.filter((q) => q.difficulty === 'medium');
    startIndex = ((level - 13) * perLevel) % pool.length;
  } else {
    pool = missingPlayerQuestions.filter((q) => q.difficulty === 'hard');
    startIndex = ((Math.max(level, 19) - 19) * perLevel) % pool.length;
  }
  const out = [];
  for (let i = 0; i < perLevel; i++) {
    out.push(pool[(startIndex + i) % pool.length]);
  }
  return out;
};

