/**
 * Snapshot Missing Player level 30 into a frozen module + SHA-256 manifest.
 *
 * Regenerate ONLY when intentionally re-locking after verified layout changes:
 *   node scripts/freeze-missing-player-level-30.mjs --confirm
 *
 * Without --confirm, prints what would change and exits (dry run).
 */
import crypto from 'crypto';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const SOURCE = path.join(ROOT, 'src/data/mediumLineups/level30.ts');
const FROZEN = path.join(ROOT, 'src/data/missingPlayerLineupLevels30.frozen.ts');
const MANIFEST = path.join(ROOT, 'src/data/missingPlayerLineupLevels30.frozen.sha256');

function extractLevel30ArrayBody(sourceText) {
  const startMarker = 'export const LEVEL_30: MissingPlayerLineupMatchDef[] = [';
  const start = sourceText.indexOf(startMarker);
  if (start < 0) throw new Error('Could not locate LEVEL_30 array in mediumLineups/level30.ts');
  const afterStart = start + startMarker.length;
  const end = sourceText.lastIndexOf('\n];');
  if (end < 0 || end <= afterStart) throw new Error('Could not locate end of LEVEL_30 array');
  return sourceText.slice(afterStart, end).trim();
}

function buildFrozenSource(arrayBody) {
  return `/**
 * FROZEN — Missing Player level 30 (World Cup 1930–1966 vintage mix — Wikipedia-aligned pitch layouts).
 *
 * DO NOT EDIT THIS FILE BY HAND.
 * Level 30 is locked; work on level 31+ in mediumLineups/.
 *
 * To regenerate after an intentional re-lock:
 *   node scripts/freeze-missing-player-level-30.mjs --confirm
 */
import type { MissingPlayerLineupMatchDef } from './missingPlayerLineupMatchTypes';

export const LEVEL_30: MissingPlayerLineupMatchDef[] = [
${arrayBody}
];

/** SHA-256 of LEVEL_30 match-def payload. Validated at dev/build. */
export const MISSING_PLAYER_LEVEL_30_PAYLOAD_SHA256 = '__PAYLOAD_SHA__';
`;
}

function payloadSha(sourceWithoutHashLine) {
  const m = /export const LEVEL_30[\s\S]*?= \[([\s\S]*?)\];/.exec(sourceWithoutHashLine);
  if (!m) throw new Error('Missing LEVEL_30 in generated source');
  return crypto.createHash('sha256').update(m[1].replace(/\s+/g, ' ').trim()).digest('hex');
}

const confirm = process.argv.includes('--confirm');
const rehash = process.argv.includes('--rehash');

if (rehash) {
  if (!fs.existsSync(FROZEN)) {
    console.error('No frozen file to rehash. Run with --confirm first.');
    process.exit(1);
  }
  let draft = fs.readFileSync(FROZEN, 'utf8');
  const stripped = draft.replace(/export const MISSING_PLAYER_LEVEL_30_PAYLOAD_SHA256 = '[^']+';/, '');
  const hash = payloadSha(stripped);
  draft = draft.replace(
    /export const MISSING_PLAYER_LEVEL_30_PAYLOAD_SHA256 = '[^']+';/,
    `export const MISSING_PLAYER_LEVEL_30_PAYLOAD_SHA256 = '${hash}';`,
  );
  fs.writeFileSync(FROZEN, draft);
  fs.writeFileSync(MANIFEST, `${hash}  src/data/missingPlayerLineupLevels30.frozen.ts\n`);
  console.log(`Rehashed frozen file. Payload SHA-256: ${hash}`);
  process.exit(0);
}

const sourceText = fs.readFileSync(SOURCE, 'utf8');
if (!sourceText.includes('export const LEVEL_30: MissingPlayerLineupMatchDef[] = [')) {
  throw new Error(
    'level30.ts is a re-export stub. Edit missingPlayerLineupLevels30.frozen.ts and run --rehash, or restore full LEVEL_30 in level30.ts before re-freezing.',
  );
}

const arrayBody = extractLevel30ArrayBody(sourceText);
if (!arrayBody.includes("id: 'level-30-match-1'")) {
  throw new Error('LEVEL_30 extraction looks invalid');
}

let draft = buildFrozenSource(arrayBody);
const hash = payloadSha(draft);
draft = draft.replace('__PAYLOAD_SHA__', hash);

const existing = fs.existsSync(FROZEN) ? fs.readFileSync(FROZEN, 'utf8') : null;
if (existing === draft) {
  console.log('Frozen level 30 already up to date.');
  process.exit(0);
}

if (!confirm) {
  console.log('Dry run — level 30 snapshot would change.');
  console.log(`  Output: ${path.relative(ROOT, FROZEN)}`);
  console.log(`  Payload SHA-256: ${hash}`);
  console.log('\nRe-run with --confirm to write the frozen file.');
  process.exit(1);
}

fs.writeFileSync(FROZEN, draft);
fs.writeFileSync(MANIFEST, `${hash}  src/data/missingPlayerLineupLevels30.frozen.ts\n`);
console.log(`Wrote ${path.relative(ROOT, FROZEN)}`);
console.log(`Payload SHA-256: ${hash}`);
