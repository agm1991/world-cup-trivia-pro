function fmtQuestion(q) {
  const lines = [
    '    {',
    `      id: '${q.id}',`,
    `      category: 'player',`,
    `      difficulty: '${q.difficulty}',`,
    `      eventYear: ${q.eventYear},`,
    `      question: ${JSON.stringify(q.question)},`,
    `      optionA: ${JSON.stringify(q.optionA)},`,
    `      optionB: ${JSON.stringify(q.optionB)},`,
    `      optionC: ${JSON.stringify(q.optionC)},`,
    `      optionD: ${JSON.stringify(q.optionD)},`,
    `      correctAnswer: '${q.correctAnswer}',`,
    `      hint1: ${JSON.stringify(q.hint1)},`,
    `      hint2: ${JSON.stringify(q.hint2)},`,
    `      hint3: ${JSON.stringify(q.hint3)},`,
    '    },',
  ];
  return lines.join('\n');
}

function fmtArr(name, arr) {
  return `  '${name}': [\n${arr.map(fmtQuestion).join('\n')}\n  ],`;
}

