let content = fs.readFileSync(FILE, 'utf8');
let lineupCount = 0;
let coordUpdates = 0;

content = content.replace(
  /(\{\s*\n\s*id:\s*(\d+),[\s\S]*?year:\s*(\d+),[\s\S]*?team:\s*\"([^\"]+)\"[\s\S]*?positions:\s*\[)([\s\S]*?)(\],\s*\n\s*missingIndex)/g,
  (full, pre, idStr, yearStr, team, posBlock, post) => {
    const key = `${idStr}|${team}|${yearStr}`;
    const roleCoords = CUSTOM_COORDS[key] ?? LINEUP_ROLES[key];
    if (!roleCoords) return full;

    let slot = 0;
    const nameFix = NAME_FIXES[key] ?? {};
    const newPos = posBlock.replace(
      /(\{\s*name:\s*")([^"]+)("(?:,\s*flag:[^}]+)?,\s*x:\s*)\d+(,\s*y:\s*)\d+/g,
      (m, p1, name, p3, p4) => {
        const fixedName = nameFix[name] ?? name;
        const [x, y] = roleCoords[slot++];
        coordUpdates++;
        return `${p1}${fixedName}${p3}${x}${p4}${y}`;
      },
    );

    if (slot !== 11) throw new Error(`${key}: expected 11 slots, got ${slot}`);
    lineupCount++;
    return pre + newPos + post;
  },
);

