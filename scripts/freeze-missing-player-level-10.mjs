/**
 * Snapshot Missing Player level 10 into a frozen module + SHA-256 manifest.
 *
 * Regenerate ONLY when intentionally re-locking after verified layout changes:
 *   node scripts/freeze-missing-player-level-10.mjs --confirm
 *
 * Without --confirm, prints what would change and exits (dry run).
 */
import crypto from 'crypto';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const SOURCE = path.join(ROOT, 'src/data/mediumLineups/level10.ts');
const OUT = path.join(ROOT, 'src/data/missingPlayerLineupLevels10.frozen.ts');
const MANIFEST = path.join(ROOT, 'src/data/missingPlayerLineupLevels10.frozen.sha256');

function extractLevel10ArrayBody(sourceText) {
  const startMarker = 'export const LEVEL_10: MissingPlayerLineupMatchDef[] = [';
  const start = sourceText.indexOf(startMarker);
  if (start < 0) throw new Error('Could not locate LEVEL_10 in mediumLineups/level10.ts');
  const afterStart = start + startMarker.length;
  const end = sourceText.lastIndexOf('\n];');
  if (end < 0 || end <= afterStart) throw new Error('Could not locate end of LEVEL_10 array');
  return sourceText.slice(afterStart, end).trim();
}

function buildFrozenSource(arrayBody) {
  return `/**
 * FROZEN — Missing Player level 10 (1990–2022 World Cup — Wikipedia-aligned pitch layouts).
 *
 * DO NOT EDIT THIS FILE BY HAND.
 * Level 10 is locked; work on level 11+ in mediumLineups/.
 *
 * To regenerate after an intentional re-lock:
 *   node scripts/freeze-missing-player-level-10.mjs --confirm
 */
import type { MissingPlayerLineupMatchDef } from './missingPlayerLineupMatchTypes';

export const LEVEL_10: MissingPlayerLineupMatchDef[] = [
${arrayBody}
];

/** SHA-256 of LEVEL_10 match-def payload. Validated at dev/build. */
export const MISSING_PLAYER_LEVEL_10_PAYLOAD_SHA256 = '__PAYLOAD_SHA__';
`;
}

function payloadSha(sourceWithoutHashLine) {
  const m = /export const LEVEL_10[\s\S]*?= \[([\s\S]*?)\];/.exec(sourceWithoutHashLine);
  if (!m) throw new Error('Missing LEVEL_10 in generated source');
  return crypto.createHash('sha256').update(m[1].replace(/\s+/g, ' ').trim()).digest('hex');
}

const confirm = process.argv.includes('--confirm');
const rehash = process.argv.includes('--rehash');

if (rehash) {
  if (!fs.existsSync(OUT)) {
    console.error('No frozen file to rehash. Run with --confirm first.');
    process.exit(1);
  }
  let draft = fs.readFileSync(OUT, 'utf8');
  const stripped = draft.replace(/export const MISSING_PLAYER_LEVEL_10_PAYLOAD_SHA256 = '[^']+';/, '');
  const hash = payloadSha(stripped);
  draft = draft.replace(
    /export const MISSING_PLAYER_LEVEL_10_PAYLOAD_SHA256 = '[^']+';/,
    `export const MISSING_PLAYER_LEVEL_10_PAYLOAD_SHA256 = '${hash}';`,
  );
  fs.writeFileSync(OUT, draft);
  fs.writeFileSync(MANIFEST, `${hash}  src/data/missingPlayerLineupLevels10.frozen.ts\n`);
  console.log(`Rehashed frozen file. Payload SHA-256: ${hash}`);
  process.exit(0);
}

const sourceText = fs.readFileSync(SOURCE, 'utf8');
const arrayBody = extractLevel10ArrayBody(sourceText);
if (!arrayBody.includes("id: 'level-10-match-1'")) {
  throw new Error('LEVEL_10 extraction looks invalid');
}

let draft = buildFrozenSource(arrayBody);
const hash = payloadSha(draft);
draft = draft.replace('__PAYLOAD_SHA__', hash);

const existing = fs.existsSync(OUT) ? fs.readFileSync(OUT, 'utf8') : null;
if (existing === draft) {
  console.log('Frozen level 10 already up to date.');
  process.exit(0);
}

if (!confirm) {
  console.log('Dry run — level 10 snapshot would change.');
  console.log(`  Output: ${path.relative(ROOT, OUT)}`);
  console.log(`  Payload SHA-256: ${hash}`);
  console.log('\nRe-run with --confirm to write the frozen file.');
  process.exit(1);
}

fs.writeFileSync(OUT, draft);
fs.writeFileSync(MANIFEST, `${hash}  src/data/missingPlayerLineupLevels10.frozen.ts\n`);
console.log(`Wrote ${path.relative(ROOT, OUT)}`);
console.log(`Payload SHA-256: ${hash}`);
