/**
 * Snapshot Missing Player level 8 into a frozen module + SHA-256 manifest.
 *
 * Regenerate ONLY when intentionally re-locking after verified layout changes:
 *   node scripts/freeze-missing-player-level-8.mjs --confirm
 *
 * Without --confirm, prints what would change and exits (dry run).
 */
import crypto from 'crypto';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const SOURCE = path.join(ROOT, 'src/data/missingPlayerLineupLevels7to17.ts');
const OUT = path.join(ROOT, 'src/data/missingPlayerLineupLevels8.frozen.ts');
const MANIFEST = path.join(ROOT, 'src/data/missingPlayerLineupLevels8.frozen.sha256');

function extractLevel8ArrayBody(sourceText) {
  const startMarker = 'const LEVEL_8: MissingPlayerLineupMatchDef[] = [';
  const start = sourceText.indexOf(startMarker);
  if (start < 0) throw new Error('Could not locate LEVEL_8 in missingPlayerLineupLevels7to17.ts');
  const afterStart = start + startMarker.length;
  const endMarker = '\n];\n\n// ——— Level 9 ———';
  const end = sourceText.indexOf(endMarker, afterStart);
  if (end < 0) throw new Error('Could not locate end of LEVEL_8 array');
  return sourceText.slice(afterStart, end).trim();
}

function buildFrozenSource(arrayBody) {
  return `/**
 * FROZEN — Missing Player level 8 (2010/2014/2018 World Cup QF/SF/R16 — Wikipedia-aligned pitch layouts).
 *
 * DO NOT EDIT THIS FILE BY HAND.
 * Level 8 is locked; work on level 9+ in missingPlayerLineupLevels7to17.ts / mediumLineups/.
 *
 * To regenerate after an intentional re-lock:
 *   node scripts/freeze-missing-player-level-8.mjs --confirm
 */
import type { MissingPlayerLineupMatchDef } from './missingPlayerLineupMatchTypes';

export const LEVEL_8: MissingPlayerLineupMatchDef[] = [
${arrayBody}
];

/** SHA-256 of LEVEL_8 match-def payload. Validated at dev/build. */
export const MISSING_PLAYER_LEVEL_8_PAYLOAD_SHA256 = '__PAYLOAD_SHA__';
`;
}

function payloadSha(sourceWithoutHashLine) {
  const m = /export const LEVEL_8[\s\S]*?= \[([\s\S]*?)\];/.exec(sourceWithoutHashLine);
  if (!m) throw new Error('Missing LEVEL_8 in generated source');
  return crypto.createHash('sha256').update(m[1].replace(/\s+/g, ' ').trim()).digest('hex');
}

const confirm = process.argv.includes('--confirm');
const rehash = process.argv.includes('--rehash');

if (rehash) {
  if (!fs.existsSync(OUT)) {
    console.error('No frozen file to rehash. Run with --confirm first.');
    process.exit(1);
  }
  let draft = fs.readFileSync(OUT, 'utf8');
  const stripped = draft.replace(/export const MISSING_PLAYER_LEVEL_8_PAYLOAD_SHA256 = '[^']+';/, '');
  const hash = payloadSha(stripped);
  draft = draft.replace(
    /export const MISSING_PLAYER_LEVEL_8_PAYLOAD_SHA256 = '[^']+';/,
    `export const MISSING_PLAYER_LEVEL_8_PAYLOAD_SHA256 = '${hash}';`,
  );
  fs.writeFileSync(OUT, draft);
  fs.writeFileSync(MANIFEST, `${hash}  src/data/missingPlayerLineupLevels8.frozen.ts\n`);
  console.log(`Rehashed frozen file. Payload SHA-256: ${hash}`);
  process.exit(0);
}

const sourceText = fs.readFileSync(SOURCE, 'utf8');
const arrayBody = extractLevel8ArrayBody(sourceText);
if (!arrayBody.includes("id: 'level-8-match-1'")) {
  throw new Error('LEVEL_8 extraction looks invalid');
}

let draft = buildFrozenSource(arrayBody);
const hash = payloadSha(draft);
draft = draft.replace('__PAYLOAD_SHA__', hash);

const existing = fs.existsSync(OUT) ? fs.readFileSync(OUT, 'utf8') : null;
if (existing === draft) {
  console.log('Frozen level 8 already up to date.');
  process.exit(0);
}

if (!confirm) {
  console.log('Dry run — level 8 snapshot would change.');
  console.log(`  Output: ${path.relative(ROOT, OUT)}`);
  console.log(`  Payload SHA-256: ${hash}`);
  console.log('\nRe-run with --confirm to write the frozen file.');
  process.exit(1);
}

fs.writeFileSync(OUT, draft);
fs.writeFileSync(MANIFEST, `${hash}  src/data/missingPlayerLineupLevels8.frozen.ts\n`);
console.log(`Wrote ${path.relative(ROOT, OUT)}`);
console.log(`Payload SHA-256: ${hash}`);
