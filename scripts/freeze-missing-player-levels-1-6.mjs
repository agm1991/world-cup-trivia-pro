/**
 * Snapshot Missing Player levels 1–6 into a frozen module + SHA-256 manifest.
 *
 * Regenerate ONLY when intentionally re-locking after verified layout changes:
 *   node scripts/freeze-missing-player-levels-1-6.mjs --confirm
 *
 * Without --confirm, prints what would change and exits (dry run).
 */
import crypto from 'crypto';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const GAME = path.join(ROOT, 'src/pages/MissingPlayerGame.tsx');
const OUT = path.join(ROOT, 'src/data/missingPlayerLineupLevels1to6.frozen.ts');
const MANIFEST = path.join(ROOT, 'src/data/missingPlayerLineupLevels1to6.frozen.sha256');

const LEVEL_1_ORDER = [0, 2, 3, 4, 6, 7, 10, 12, 14, 70];
const LEVEL_2_ORDER = [20, 22, 23, 24, 25, 30, 32, 33, 40, 41];
const LEVEL_3_ORDER = [50, 52, 53, 54, 55, 57, 19, 16, 17, 70];

function extractQuestionBlocks(text) {
  const start = text.indexOf('const allLineupQuestions');
  const end = text.indexOf('const MISSING_PLAYER_LEVEL_1_ORDER');
  if (start < 0 || end < 0) throw new Error('Could not locate allLineupQuestions in MissingPlayerGame.tsx');
  const body = text.slice(start, end);
  const blocks = [];
  const re = /\{\s*\n\s*id:\s*(\d+),[\s\S]*?\n\s*hint3:[^\n]+\n\s*\}/g;
  for (const m of body.matchAll(re)) blocks.push(m[0]);
  return blocks;
}

function levelQuestions(blocks, level) {
  if (level === 1) return LEVEL_1_ORDER.map((i) => blocks[i]);
  if (level === 2) return LEVEL_2_ORDER.map((i) => blocks[i]);
  if (level === 3) return LEVEL_3_ORDER.map((i) => blocks[i]);
  if (level >= 4 && level <= 6) {
    const base = 30 + (level - 4) * 10;
    return blocks.slice(base, base + 10);
  }
  throw new Error(`Invalid level: ${level}`);
}

function buildFrozenSource(blocks) {
  const levels = [];
  for (let level = 1; level <= 6; level++) {
    const qs = levelQuestions(blocks, level);
    if (qs.length !== 10) throw new Error(`Level ${level}: expected 10 questions, got ${qs.length}`);
    levels.push(`export const MISSING_PLAYER_LEVEL_${level}: MissingPlayerLineupGameQuestion[] = [\n  ${qs.join(',\n  ')},\n];`);
  }

  const body = levels.join('\n\n');

  return `/**
 * FROZEN — Missing Player levels 1–6 (Wikipedia-aligned pitch layouts).
 *
 * DO NOT EDIT THIS FILE BY HAND.
 * Levels 1–6 are locked; work on level 7+ in missingPlayerLineupLevels7to17.ts / mediumLineups/.
 *
 * To regenerate after an intentional re-lock:
 *   node scripts/freeze-missing-player-levels-1-6.mjs --confirm
 */
import type { MissingPlayerLineupGameQuestion } from './missingPlayerLineupMatchTypes';

${body}

const BY_LEVEL: Record<number, MissingPlayerLineupGameQuestion[]> = {
  1: MISSING_PLAYER_LEVEL_1,
  2: MISSING_PLAYER_LEVEL_2,
  3: MISSING_PLAYER_LEVEL_3,
  4: MISSING_PLAYER_LEVEL_4,
  5: MISSING_PLAYER_LEVEL_5,
  6: MISSING_PLAYER_LEVEL_6,
};

/** SHA-256 of this file's question payload (levels 1–6 positions only). Validated at dev/build. */
export const MISSING_PLAYER_LEVELS_1_TO_6_PAYLOAD_SHA256 = '__PAYLOAD_SHA__';

export function getMissingPlayerLineupQuestionsForLevel1to6(level: number): MissingPlayerLineupGameQuestion[] {
  return BY_LEVEL[level] ?? [];
}
`;
}

function payloadSha(sourceWithoutHashLine) {
  const payloads = [];
  for (let level = 1; level <= 6; level++) {
    const re = new RegExp(`export const MISSING_PLAYER_LEVEL_${level}[\\s\\S]*?= \\[([\\s\\S]*?)\\];`);
    const m = re.exec(sourceWithoutHashLine);
    if (!m) throw new Error(`Missing LEVEL_${level} in generated source`);
    payloads.push(m[1].replace(/\s+/g, ' ').trim());
  }
  return crypto.createHash('sha256').update(payloads.join('\n')).digest('hex');
}

const confirm = process.argv.includes('--confirm');
const rehash = process.argv.includes('--rehash');

if (rehash) {
  if (!fs.existsSync(OUT)) {
    console.error('No frozen file to rehash. Run with --confirm first.');
    process.exit(1);
  }
  let draft = fs.readFileSync(OUT, 'utf8');
  const stripped = draft.replace(/export const MISSING_PLAYER_LEVELS_1_TO_6_PAYLOAD_SHA256 = '[^']+';/, '');
  const hash = payloadSha(stripped);
  draft = draft.replace(
    /export const MISSING_PLAYER_LEVELS_1_TO_6_PAYLOAD_SHA256 = '[^']+';/,
    `export const MISSING_PLAYER_LEVELS_1_TO_6_PAYLOAD_SHA256 = '${hash}';`,
  );
  fs.writeFileSync(OUT, draft);
  fs.writeFileSync(MANIFEST, `${hash}  src/data/missingPlayerLineupLevels1to6.frozen.ts\n`);
  console.log(`Rehashed frozen file. Payload SHA-256: ${hash}`);
  process.exit(0);
}

const gameText = fs.readFileSync(GAME, 'utf8');
const blocks = extractQuestionBlocks(gameText);
if (blocks.length < 71) {
  throw new Error(`Expected ≥71 lineup blocks, found ${blocks.length}`);
}

let draft = buildFrozenSource(blocks);
const hash = payloadSha(draft);
draft = draft.replace('__PAYLOAD_SHA__', hash);

const existing = fs.existsSync(OUT) ? fs.readFileSync(OUT, 'utf8') : null;
if (existing === draft) {
  console.log('Frozen levels 1–6 already up to date.');
  process.exit(0);
}

if (!confirm) {
  console.log('Dry run — levels 1–6 snapshot would change.');
  console.log(`  Output: ${path.relative(ROOT, OUT)}`);
  console.log(`  Payload SHA-256: ${hash}`);
  console.log('\nRe-run with --confirm to write the frozen file.');
  process.exit(1);
}

fs.writeFileSync(OUT, draft);
fs.writeFileSync(MANIFEST, `${hash}  src/data/missingPlayerLineupLevels1to6.frozen.ts\n`);
console.log(`Wrote ${path.relative(ROOT, OUT)}`);
console.log(`Payload SHA-256: ${hash}`);
